import type { CSSProperties, ReactNode } from "react";

interface SvgIconProps {
  size?: number;
  width?: number;
  height?: number;
  className?: string;
  style?: CSSProperties;
  editorOnly?: boolean;
}

interface SvgSpec {
  viewBoxWidth: number;
  viewBoxHeight: number;
  children: ReactNode;
}

function SvgIcon({
  spec,
  size,
  width,
  height,
  className,
  style,
  editorOnly,
}: SvgIconProps & { spec: SvgSpec }) {
  const { viewBoxWidth, viewBoxHeight, children } = spec;
  const displayWidth = width ?? size ?? viewBoxWidth;
  const displayHeight =
    height ??
    (size != null
      ? (viewBoxHeight / viewBoxWidth) * displayWidth
      : viewBoxHeight);

  return (
    <span
      className={className}
      aria-hidden
      data-keyboard-editor-icon={editorOnly ? "" : undefined}
      style={{
        display: "inline-flex",
        alignItems: "center",
        justifyContent: "center",
        lineHeight: 0,
        ...style,
      }}
    >
      <svg
        data-export-icon
        width={displayWidth}
        height={displayHeight}
        viewBox={`0 0 ${viewBoxWidth} ${viewBoxHeight}`}
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        style={{ display: "block" }}
      >
        {children}
      </svg>
    </span>
  );
}

function makeIcon(spec: SvgSpec) {
  return function Icon(props: SvgIconProps) {
    return <SvgIcon spec={spec} {...props} />;
  };
}

